# Wszystko jest obiektem. Do sprawdzenia jakie sa dostępne funkcje w obiektcie służy dir()
string = 'string'
print(dir(string))

# aby sprawdzic typ zmiennej uzywamy type()
print(type(string))

float = 4.25
print(dir(float))
print(type(float))

# booleany piszemy zawsze z dużej
print(dir(True))
print(type(True))
print(dir(False))
print(type(False))